#creating vector A
A=[2,-7,6]
#displaying vector A
print('A:',A)
#creating vector B
B=[3,2 ,-7]
#displaying vector B
print('B:',B)
#calculating sum of A and B
A_add_B=[A[0]+B[0],A[1]+B[1],A[2]+B[2]]
#displaying vector addition
print('A+B:',A_add_B)      # output A+B:[5,-5,-1]
#scalar dot product
A_dot_B=(A[0]*B[0])+(A[1]*B[1])+(A[2]*B[2])
#displaying dot product
print('A.B:',A_dot_B)      # output A.B:-50
